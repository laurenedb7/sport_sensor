                <h2> Comment s'inscrire ? </h2>

                <h3> Inscription du coach </h3>
                <p> Dans un premier temps, le coach doit s'insccrire, en cliquant sur l'onglet "coach". Alors, le coach va devoir payer une license. </p>
                <p> La license contient un accès au site pour le coach et l'ensemble de ses sportifs, mais également des boitiers permettant de réaliser les différents tests psychotechniques présentés précédemment sur la page visiteurs. </p>
                <p> Une fois le paiement effectué, il doit rentrer un certains nombres d'informations et choisir un pseudo et un mot de passe. Enfin, il va recevoir un code d'équipe en tant que coach. Ce code servira aux sportifs de l'équipe de s'inscrire à leur tour sur la plateforme. </p>

                <h3> Inscription des sportifs </h3>
                <p> Une fois le coach inscrit, les sportifs de l'équipe vont pouvoir à leur tour s'inscrire sur la plateforme. En cliquant sur l'onglet "sportif", ils vont devoir rentrer un code d'équipe, celui que leur coach a reçu. Ainsi, ils pourront s'insccrire. </p>

