                <h3> Quelles informations sont nécessaires pour s'inscrire sur la plateforme ? </h3>
                <p> Le coach et les sportifs doivent renseigner leur nom, prénom, date de naissance, adresse mail, numéro de téléphone et sport pratiqué. Les sportifs doivent également rentrer leur poids et leur taille. </p>
                <p> Ensuite, ils doivent charger une photo, la plus récente possible. Ils choisissent un pseudo ainsi qu'un mot de passe. Une confirmation d'inscription est envoyée automatiquement par mail. </p>
